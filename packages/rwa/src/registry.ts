export function getAsset(ticker: string): OndoAsset {
  const asset = ONDO_REGISTRY[ticker.toUpperCase()];
  if (!asset) throw new Error(`Unknown Ondo ticker: ${ticker}. Available: ${Object.keys(ONDO_REGISTRY).join(', ')}`);
  return asset;
}

export function listTickers(): string[] {
  return Object.keys(ONDO_REGISTRY);
}

export function isValidTicker(ticker: string): boolean {
  return ticker.toUpperCase() in ONDO_REGISTRY;
}
